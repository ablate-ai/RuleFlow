// ===== API 工具 =====
window.api = {
  async request(url, options = {}) {
    const res = await fetch(url, {
      headers: { 'Content-Type': 'application/json' },
      ...options,
    });
    if (!res.ok) {
      const text = await res.text();
      throw new Error(text || `请求失败: ${res.status}`);
    }
    return res.json();
  },
  get:    (url)       => window.api.request(url),
  post:   (url, data) => window.api.request(url, { method: 'POST',   body: JSON.stringify(data) }),
  put:    (url, data) => window.api.request(url, { method: 'PUT',    body: JSON.stringify(data) }),
  patch:  (url, data) => window.api.request(url, { method: 'PATCH',  body: JSON.stringify(data) }),
  delete: (url)       => window.api.request(url, { method: 'DELETE' }),
};

// ===== 时间格式化 =====
window.formatDate = (str) => {
  if (!str) return '-';
  return new Date(str).toLocaleString('zh-CN', {
    year: 'numeric', month: '2-digit', day: '2-digit',
    hour: '2-digit', minute: '2-digit',
  }).replace(/\//g, '-');
};

